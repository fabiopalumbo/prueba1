    public void autoIncremental() {
        if (core.getDocumentos().isEmpty()) {
            jTFCodigoCompra.setText(0 + "");
        } else {
            int auto = core.getDocumentos().get(core.getDocumentos().size() - 1).getCodigo() + 1;
            jTFCodigoCompra.setText(auto + "");
        }
    }

    /**
     * limpia campos al realizar una compra
     */
    public void limpiarCamposCrear() {
        jTFCodigoBien.setText("");
        jTFCantidad.setText("");
        jTFNombreBien.setText("");
        jTFTotal.setText(0 + "");
        jTFVelor.setText("");
        jTAInfo.setText("");
    }

