    /**
     * refresca la tabla de compras
     */
    public void refrescarTabla() {
        DefaultTableModel dtm = (DefaultTableModel) jTInfoVentas.getModel();
        dtm.setRowCount(0);
        for (int i = 0; i < core.getDocumentos().size(); i++) {
            if (core.getDocumentos().get(i) instanceof Compra) {
                Compra com = (Compra) core.getDocumentos().get(i);
                dtm.addRow(new Object[]{com.getCodigo(), com.getFechaCompra(), com.getTotalCompra()});
            }
        }
    }

