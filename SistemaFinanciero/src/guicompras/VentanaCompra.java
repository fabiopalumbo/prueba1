    private void jBEliminarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarCompraActionPerformed
        // TODO add your handling code here:
        try {
            int fila = jTInfoVentas.getSelectedRow();
            if (fila >= 0) {
                int valor = JOptionPane.showConfirmDialog(null, "¿Desea eliminar la compra?", "Eliminar compra", JOptionPane.YES_NO_OPTION);
                if (valor == 0) {
                    int idCompra = (int) jTInfoVentas.getValueAt(fila, 0);
                    for (int i = 0; i < core.getDocumentos().size(); i++) {
                        if (core.getDocumentos().get(i) instanceof Compra && core.getDocumentos().get(i).getCodigo() == idCompra) {
                            core.eliminarDocumento(core.getDocumentos().get(i));
                        }
                    }
                    refrescarTabla();
                    autoIncremental();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Selecione una compra");
            }
        } catch (ClassCastException e) {
            JOptionPane.showMessageDialog(null, "Error al intentar eliminar");
        }
    }//GEN-LAST:event_jBEliminarCompraActionPerformed

    private void jBMasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBMasActionPerformed
        // TODO add your handling code here:
        try {
            int fila = jTInfoVentas.getSelectedRow();
            int idCompra = (int) jTInfoVentas.getValueAt(fila, 0);
            Compra com = (Compra) core.buscarDocumento(idCompra);
            DefaultTableModel dtm = (DefaultTableModel) jTDetalles.getModel();
            dtm.setRowCount(0);
            for (int i = 0; i < com.getDetalles().size(); i++) {
                dtm.addRow(new Object[]{com.getDetalles().get(i).getBien().getNombre(),
                    com.getDetalles().get(i).getCantidadBien(), com.getDetalles().get(i).getBien().getValor()});
            }
        } catch (ClassCastException | ArrayIndexOutOfBoundsException | NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Error al intentar ver detalles");
        }
    }//GEN-LAST:event_jBMasActionPerformed

