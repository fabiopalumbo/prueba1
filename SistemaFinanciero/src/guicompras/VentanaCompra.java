    private void jBComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBComprarActionPerformed
        // TODO add your handling code here:
        try {
            Compra compra = new Compra(Calendar.getInstance().getTime(),
                    Double.parseDouble(jTFTotal.getText()), Integer.parseInt(jTFCodigoCompra.getText()));
            for (int i = 0; i < bienesAgregados.size(); i++) {
                Detalle detalle = new Detalle(compra, bienesAgregados.get(i), cantidadBien.get(i));
                compra.getDetalles().add(detalle);
            }
            bienesAgregados = new ArrayList<>();
            cantidadBien = new ArrayList<>();
            core.crearDocumento(compra);
            jTFCodigoCompra.setText(String.valueOf(core.getDocumentos().size()));
            limpiarCamposCrear();
            refrescarTabla();
            autoIncremental();
            JOptionPane.showMessageDialog(null, "La compra ha sido creada");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Se han ingresado letras en campos numericos");
        }
    }//GEN-LAST:event_jBComprarActionPerformed

