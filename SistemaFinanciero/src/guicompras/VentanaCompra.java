    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        // TODO add your handling code here:
        try {
            if (!jTFCodigoBien.getText().isEmpty() && !jTFCantidad.getText().isEmpty()
                    && !jTFNombreBien.getText().isEmpty() && !jTFVelor.getText().isEmpty()
                    && !jTAInfo.getText().isEmpty()) {
                Bien bien;
                int codigo = Integer.parseInt(jTFCodigoBien.getText());
                String nombre = jTFNombreBien.getText();
                double valor = Double.parseDouble(jTFVelor.getText());
                int cant = Integer.parseInt(jTFCantidad.getText());
                if (jRBInversion.isSelected()) {
                    Inversion in = new Inversion(jTAInfo.getText(), 123);
                    bien = new Bien(codigo, nombre, in, valor);

                } else {
                    Servicio serv = new Servicio(jTAInfo.getText(), 76152);
                    bien = new Bien(codigo, nombre, serv, valor);
                }
                cantidadBien.add(cant);
                bienesAgregados.add(bien);
                double total = Double.parseDouble(jTFTotal.getText()) + (cant * valor);
                jTFTotal.setText(total + "");
                JOptionPane.showMessageDialog(null, "El bien ha sido agregado a la compra");
            } else {
                JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Se han ingresado letras en campos numericos");
        }
    }//GEN-LAST:event_jBAgregarActionPerformed

